package com.ultramega.shop.pojo.scan;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class ScannedItem {
    @SerializedName("ItemCode")
    @Expose
    private String ItemCode;
    @SerializedName("ItemDescription")
    @Expose
    private String ItemDescription;
    @SerializedName("PackSize")
    @Expose
    private String PackSize;
    @SerializedName("Category")
    @Expose
    private String Category;
    @SerializedName("CatClass")
    @Expose
    private String CatClass;
    @SerializedName("SupplierID")
    @Expose
    private String SupplierID;
    @SerializedName("PackageCode")
    @Expose
    private String PackageCode;
    @SerializedName("Price")
    @Expose
    private double Price;
    @SerializedName("GrossPrice")
    @Expose
    private double GrossPrice;
    @SerializedName("isPromo")
    @Expose
    private int isPromo;
    @SerializedName("PromoDetails")
    @Expose
    private String PromoDetails;
    @SerializedName("PriceGroup")
    @Expose
    private String PriceGroup;
    @SerializedName("MinimumOrderQTY")
    @Expose
    private int MinimumOrderQTY;
    @SerializedName("IncrementalOrderQTY")
    @Expose
    private int IncrementalOrderQTY;
    @SerializedName("isBulk")
    @Expose
    private int isBulk;
    @SerializedName("Barcode")
    @Expose
    private String Barcode;
    @SerializedName("PackageDescription")
    @Expose
    private String PackageDescription;
    @SerializedName("Description")
    @Expose
    private String Description;
    @SerializedName("BarcodeFrom")
    @Expose
    private String BarcodeFrom;
    @SerializedName("ItemPicURL")
    @Expose
    private String ItemPicURL;

    public ScannedItem(String itemCode, String itemDescription, String packSize, String category, String catClass, String supplierID, String packageCode, double price, double grossPrice, int isPromo, String promoDetails, String priceGroup, int minimumOrderQTY, int incrementalOrderQTY, int isBulk, String barcode, String packageDescription, String description, String barcodeFrom, String itemPicURL) {
        ItemCode = itemCode;
        ItemDescription = itemDescription;
        PackSize = packSize;
        Category = category;
        CatClass = catClass;
        SupplierID = supplierID;
        PackageCode = packageCode;
        Price = price;
        GrossPrice = grossPrice;
        this.isPromo = isPromo;
        PromoDetails = promoDetails;
        PriceGroup = priceGroup;
        MinimumOrderQTY = minimumOrderQTY;
        IncrementalOrderQTY = incrementalOrderQTY;
        this.isBulk = isBulk;
        Barcode = barcode;
        PackageDescription = packageDescription;
        Description = description;
        BarcodeFrom = barcodeFrom;
        ItemPicURL = itemPicURL;
    }

    public String getItemCode() {
        return ItemCode;
    }

    public String getItemDescription() {
        return ItemDescription;
    }

    public String getPackSize() {
        return PackSize;
    }

    public String getCategory() {
        return Category;
    }

    public String getCatClass() {
        return CatClass;
    }

    public String getSupplierID() {
        return SupplierID;
    }

    public String getPackageCode() {
        return PackageCode;
    }

    public double getPrice() {
        return Price;
    }

    public double getGrossPrice() {
        return GrossPrice;
    }

    public int getIsPromo() {
        return isPromo;
    }

    public String getPromoDetails() {
        return PromoDetails;
    }

    public String getPriceGroup() {
        return PriceGroup;
    }

    public int getMinimumOrderQTY() {
        return MinimumOrderQTY;
    }

    public int getIncrementalOrderQTY() {
        return IncrementalOrderQTY;
    }

    public int getIsBulk() {
        return isBulk;
    }

    public String getBarcode() {
        return Barcode;
    }

    public String getPackageDescription() {
        return PackageDescription;
    }

    public String getDescription() {
        return Description;
    }

    public String getBarcodeFrom() {
        return BarcodeFrom;
    }

    public String getItemPicURL() {
        return ItemPicURL;
    }
}
