package com.ultramega.shop.pojo;


public class PickUpBranches {

    private String branchName;
    private String branchAddress;
    private String branchDistance;
    private double latitude;
    private double longitude;

    public PickUpBranches(){
        super();
    }

    public PickUpBranches(String branchName, String branchAddress, String branchDistance, double latitude, double longitude) {
        this.branchName = branchName;
        this.branchAddress = branchAddress;
        this.branchDistance = branchDistance;
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public String getBranchName() {
        return branchName;
    }

    public void setBranchName(String branchName) {
        this.branchName = branchName;
    }

    public String getBranchAddress() {
        return branchAddress;
    }

    public void setBranchAddress(String branchAddress) {
        this.branchAddress = branchAddress;
    }

    public String getBranchDistance() {
        return branchDistance;
    }

    public void setBranchDistance(String branchDistance) {
        this.branchDistance = branchDistance;
    }
}
