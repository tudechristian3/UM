public class BankNameReference {
    private String BankName;
    private String BankCode;

    public BankNameReference() {
        super();
    }

    public BankNameReference(String bankName, String bankCode) {
        BankName = bankName;
        BankCode = bankCode;
    }

    public String getBankName() {
        return BankName;
    }

    public void setBankName(String bankName) {
        BankName = bankName;
    }

    public String getBankCode() {
        return BankCode;
    }

    public void setBankCode(String bankCode) {
        BankCode = bankCode;
    }

    @Override
    public String toString() {
        return BankName;
    }
}
